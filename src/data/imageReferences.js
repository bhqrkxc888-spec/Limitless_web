/**
 * Get image URL by reference path
 * @param {string} path - Dot notation path (e.g., 'cruiseLines.po.hero')
 * @param {string} fallback - Fallback URL or placeholder
 * @returns {string} Image URL or fallback
 */
export function getImageByRef(path, fallback = null) {
  const keys = path.split('.');
  let value = imageReferences;
  
  for (const key of keys) {
    if (value && typeof value === 'object' && key in value) {
      value = value[key];
    } else {
      console.warn(`Image reference not found: ${path}`);
      return fallback || 'IMAGE_NEEDED';
    }
  }
  
  // If value starts with 'IMAGE_NEEDED', return fallback or the placeholder
  if (typeof value === 'string' && value.startsWith('IMAGE_NEEDED')) {
    console.warn(`Image not uploaded yet: ${path} - ${value}`);
    return fallback || value;
  }
  
  return value;
}

/**
 * Check if image reference is ready (uploaded to Vercel Blob)
 * @param {string} path - Dot notation path
 * @returns {boolean}
 */
export function isImageReady(path) {
  const url = getImageByRef(path);
  return typeof url === 'string' && 
         !url.startsWith('IMAGE_NEEDED') && 
         (url.includes('vercel-storage.com') || url.includes('blob.vercel-storage.com'));
}

