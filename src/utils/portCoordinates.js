/**
 * Calculate geographic distance between two coordinates (Haversine formula)
 * Returns distance in kilometers
 */
function calculateDistance(lat1, lon1, lat2, lon2) {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

/**
 * Get coordinates for a port by name with smart disambiguation
 * 
 * NEW FEATURES:
 * - Returns multiple matches if ambiguous (e.g., "Sydney" returns both Australia and Nova Scotia)
 * - Supports geographic proximity matching to pick the best match
 * - Handles regional variants (e.g., "Sydney, Nova Scotia" vs "Sydney, Australia")
 * 
 * @param {string} portName - Name of the port to look up
 * @param {number} contextLat - Latitude of a nearby port (for proximity matching)
 * @param {number} contextLon - Longitude of a nearby port (for proximity matching)
 * @returns {Object|Array|null} - Single match, array of matches, or null
 */
export function getPortCoordinates(portName, contextLat = null, contextLon = null) {
  if (!portName) return null;
  
  const normalized = portName.toLowerCase().trim();
  
  // 1. EXACT MATCH (highest priority)
  // E.g., "Sydney, Nova Scotia" → exact match
  const exactMatch = Object.keys(KNOWN_PORT_COORDINATES).find(
    key => key.toLowerCase() === normalized
  );
  if (exactMatch) {
    return { ...KNOWN_PORT_COORDINATES[exactMatch], name: exactMatch };
  }
  
  // 2. COLLECT ALL PARTIAL MATCHES
  // E.g., "Sydney" → ["Sydney", "Sydney, Nova Scotia", "Sydney, Australia"]
  // E.g., "Perfect Day at CocoCay, Bahamas" → ["CocoCay"] (search contains key)
  const allMatches = Object.keys(KNOWN_PORT_COORDINATES).filter(key => {
    const keyLower = key.toLowerCase();
    const keyBase = keyLower.split(',')[0].trim(); // "Sydney, Nova Scotia" → "sydney"
    const searchBase = normalized.split(',')[0].trim();
    
    // Match if:
    // - Key contains search (e.g., "Sydney, Nova Scotia" contains "Sydney")
    // - OR base names match (e.g., "Sydney" base = "sydney")
    // - OR search contains key (e.g., "Perfect Day at CocoCay" contains "CocoCay") ← NEW!
    // - OR search contains key base (e.g., "Perfect Day at CocoCay, Bahamas" contains "cococay")
    return keyLower.includes(searchBase) || 
           keyBase === searchBase ||
           searchBase.includes(keyBase) ||
           normalized.includes(keyBase);
  });
  
  // 3. NO MATCHES - Try without parentheses
  if (allMatches.length === 0) {
    const withoutParens = normalized.replace(/\([^)]+\)/g, '').trim();
    if (withoutParens !== normalized) {
      const match = Object.keys(KNOWN_PORT_COORDINATES).find(
        key => key.toLowerCase().replace(/\([^)]+\)/g, '').trim() === withoutParens
      );
      if (match) {
        return { ...KNOWN_PORT_COORDINATES[match], name: match };
      }
    }
    
    // 3b. Try extracting location after "at" or "in" keywords
    // E.g., "Perfect Day at CocoCay" → try "CocoCay"
    const atMatch = normalized.match(/(?:at|in|on)\s+(.+?)(?:,|$)/i);
    if (atMatch) {
      const extracted = atMatch[1].trim();
      const extractedMatch = Object.keys(KNOWN_PORT_COORDINATES).find(
        key => key.toLowerCase() === extracted || 
               key.toLowerCase().includes(extracted) ||
               extracted.includes(key.toLowerCase())
      );
      if (extractedMatch) {
        return { ...KNOWN_PORT_COORDINATES[extractedMatch], name: extractedMatch };
      }
    }
    
    return null;
  }
  
  // 4. SINGLE MATCH - Return it
  if (allMatches.length === 1) {
    const match = allMatches[0];
    return { ...KNOWN_PORT_COORDINATES[match], name: match };
  }
  
  // 5. MULTIPLE MATCHES - Use geographic proximity if context provided
  if (contextLat !== null && contextLon !== null) {
    // Find closest match to context coordinates
    let closestMatch = null;
    let closestDistance = Infinity;
    
    for (const match of allMatches) {
      const coords = KNOWN_PORT_COORDINATES[match];
      const distance = calculateDistance(contextLat, contextLon, coords.lat, coords.lon);
      
      if (distance < closestDistance) {
        closestDistance = distance;
        closestMatch = match;
      }
    }
    
    if (closestMatch) {
      console.log(`🎯 Disambiguated "${portName}": picked ${closestMatch} (${closestDistance.toFixed(0)}km from context)`);
      return { ...KNOWN_PORT_COORDINATES[closestMatch], name: closestMatch, _disambiguated: true, _distance: closestDistance };
    }
  }
  
  // 6. MULTIPLE MATCHES - Return all for manual selection
  console.warn(`⚠️ Ambiguous port "${portName}" - found ${allMatches.length} matches:`, allMatches);
  return allMatches.map(match => ({
    ...KNOWN_PORT_COORDINATES[match],
    name: match,
    _ambiguous: true
  }));
}
